function button_mi14_history() {
    document.getElementById("mi-14-history").classList.remove("hidden");
    if (document.getElementById("mi-14-history").classList.contains('active') != 1){
        document.getElementById("mi-14-history").classList.toggle("active");
    }

    if (document.getElementById("mi-14-main").classList.contains('hidden') != 1){
        document.getElementById("mi-14-main").classList.toggle("hidden");
        document.getElementById("mi-14-main").classList.remove("active");
    }
}
